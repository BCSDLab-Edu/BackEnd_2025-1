package com.example.bcsd;

public class User {
    private String name;
    private Number age;

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(Number age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public Number getAge() {
        return age;
    }

}