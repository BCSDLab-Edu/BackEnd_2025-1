package com.example.bcsd;


public class Board {
    private Long ID;
    private String board;


    public void setID(Long ID) {
        this.ID = ID;
    }

    public void setBoard(String board) {
        this.board = board;
    }


    public Long getID() {
        return ID;
    }

    public String getBoard() {
        return board;
    }


}
